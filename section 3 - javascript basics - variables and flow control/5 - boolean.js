let temp = 31

// ===  : equality operator
let isFreezing = temp === 31
console.log(isFreezing)

// !==  : no equal operator
isFreezing = temp !== 32
console.log(isFreezing)

// <    : less than
isFreezing = temp < 32
console.log(isFreezing)

// <=   : less than or equal
isFreezing = temp <= 31
console.log(isFreezing)

// >    : greater than
isFreezing = temp > 31
console.log(isFreezing)

// >=   : greater than or equal
isFreezing = temp >= 31
console.log(isFreezing)

