let fahrenheit = 50
console.log('fahrenheit: ', fahrenheit)

// calculate the celcius and store in celcius variable
let celcius = (fahrenheit - 32) * 5 / 9
// print the value
console.log('converted to celcius: ', celcius)

// calculate the kelvin and store in kelvin variable
let kelvin = (fahrenheit + 459.67) * 5 / 9
// print the value
console.log('converted to kelvin: ', kelvin)
